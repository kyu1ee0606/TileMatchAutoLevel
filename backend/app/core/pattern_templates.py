def validate_templates() -> Dict[str, any]:
    """
    Validate all templates and return statistics.

    Returns:
        Dictionary with validation results
    """
    results = {
        'total': len(PATTERN_TEMPLATES),
        'valid': 0,
        'invalid': [],
        'tile_counts': {}
    }

    for idx, pattern in PATTERN_TEMPLATES.items():
        name = pattern.get('name', f'pattern_{idx}')
        valid = True

        for size in ['small', 'medium', 'large']:
            template = pattern.get(size)
            if not template:
                results['invalid'].append(f"{name} missing {size} template")
                valid = False
                continue

            # Count tiles
            tile_count = sum(row.count('#') for row in template)
            if name not in results['tile_counts']:
                results['tile_counts'][name] = {}
            results['tile_counts'][name][size] = tile_count

        if valid:
            results['valid'] += 1

    return results



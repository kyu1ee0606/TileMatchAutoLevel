    print(f"Valid patterns: {results['valid']}")
    if results['invalid']:
        print(f"\nInvalid patterns:")
        for issue in results['invalid']:
            print(f"  - {issue}")
    print(f"\nTile counts by pattern:")
    for name, counts in results['tile_counts'].items():
        print(f"  {name}: {counts}")
